inline const uint8_t* lookup(char c)
{
    if (c >= 'a' && c <= 'z') {
        c = static_cast<char>(c - 'a' + 'A');
    }

    for (const auto& g : FONT_TITLE_8X12) {
        if (g.c == c) {
            return g.row;
        }
    }

    return FONT_TITLE_8X12[0].row;
}

