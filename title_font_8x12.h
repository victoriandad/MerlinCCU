/// @brief Returns the title-font bitmap rows for one character.
/// @details Missing characters fall back to the first entry so title rendering never receives a
/// null row pointer.
inline const uint8_t* lookup(char c)
{
    for (const auto& g : kFontTitle8x12Glyphs)
    {
        if (g.c == c)
        {
            return g.row;
        }
    }

    return kFontTitle8x12Glyphs[0].row;
}

